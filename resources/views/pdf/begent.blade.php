        <h2>Интервальное голодание</h2>
        <div class="highlight">
            <p>Интервальное голодание — это режим питания, при котором чередуются периоды приёма пищи и голодания. Это не
                ограничение по продуктам, а по времени приёма пищи.</p>
            <h2>Популярные схемы ИГ:</h2>
            <ul>
                <li><strong>16/8</strong> — 16 часов голодания, 8 часов для еды (например, с 12:00 до 20:00).</li>
                <li><strong>14/10</strong> — мягкий вариант, особенно для начинающих.</li>
                <li><strong>5:2</strong> — 5 дней обычного питания, 2 дня с ограничением калорий (500–600 ккал).</li>
            </ul>

            <h2>Польза интервального голодания:</h2>
            <ul>
                <li>Снижение веса и жира без строгих диет.</li>
                <li>Стабилизация сахара в крови, улучшение чувствительности к инсулину.</li>
                <li>Снижение уровня воспалений в организме.</li>
                <li>Поддержка здоровья сердечно-сосудистой системы.</li>
                <li>Стимуляция аутофагии — естественного процесса обновления клеток.</li>
            </ul>

            <h2>Важные моменты:</h2>
            <ul>
                <li>Не рекомендуется беременным, людям с диабетом и нарушениями пищевого поведения.</li>
                <li>Качество еды в «окне питания» должно оставаться высоким.</li>
                <li>Начинать стоит постепенно и по возможности под наблюдением врача.</li>
            </ul>
        </div>

        <h2>Чем вреден дисбаланс БЖУ</h2>
        <div class="highlight">
            <h2>Недостаток белка:</h2>
            <ul>
                <li>Потеря мышечной массы.</li>
                <li>Слабость, утомляемость.</li>
                <li>Замедление восстановления после физической нагрузки.</li>
                <li>Нарушение иммунной функции (чаще болеешь).</li>
                <li>Проблемы с кожей, волосами, ногтями.</li>
            </ul>

            <h2>Недостаток жиров:</h2>
            <ul>
                <li>Гормональные сбои (особенно у женщин).</li>
                <li>Нарушение всасывания жирорастворимых витаминов (A, D, E, K).</li>
                <li>Сухость кожи, ломкость волос.</li>
                <li>Снижение концентрации и ухудшение настроения.</li>
                <li>Повышенный риск воспалительных заболеваний.</li>
            </ul>

            <h2>Недостаток углеводов:</h2>
            <ul>
                <li>Потеря энергии, хроническая усталость.</li>
                <li>Снижение физической и умственной работоспособности.</li>
                <li>Перенапряжение печени и почек при избытке белка как источника энергии.</li>
                <li>Нарушение пищеварения из-за нехватки клетчатки.</li>
            </ul>

            <h2>Избыток одного макронутриента (углеводов или жиров):</h2>
            <ul>
                <li>Ожирение и метаболический синдром.</li>
                <li>Инсулинорезистентность и риск диабета 2 типа.</li>
                <li>Повышенный холестерин, проблемы с сердцем.</li>
                <li>Нарушение иммунной функции (чаще болеешь).</li>
                <li>Повышенная усталость и скачки сахара в крови.</li>
            </ul>
        </div>

        <h2>Витамины и микроэлементы</h2>
